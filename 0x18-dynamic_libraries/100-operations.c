/**
 * add - add two integers
 *
 * @a: int 1
 * @b: int 2
 * Return: sum of two integers
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtract two integers
 *
 * @a: int 1
 * @b: int 2
 * Return: sum of two integers
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiply two integers
 *
 * @a: int 1
 * @b: int 2
 * Return: sum of two integers
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - div two integers
 * @a: int 1
 * @b: int 2
 * Return: sum of two integers
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - mod two integers
 * @a: int 1
 * @b: int 2
 * Return: sum of two integers
 */
int mod(int a, int b)
{
	return (a % b);
}
